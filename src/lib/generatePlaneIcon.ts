export default function generatePlaneIcon(iac: string) {
    let shape = shapes[iac];

    if (iac) {
        if (iac.startsWith("H")) {
            shape = shapes["H"];
        } else if (iac.startsWith("T")) {
            shape = shapes["T"];
        } else if (iac == "L1J-L") { // same as L2J-L
            shape = shapes["L2J-L"];
        } else if (iac == "A1P") { // same as L1P
            shape = shapes["L1P"];
        } else if (iac.startsWith("A2P") || iac.startsWith("L2T") || iac.startsWith("A2T")) {
            shape = shapes["A2P"];
        } else if (iac.startsWith("L4J")) {
            shape = shapes["L4J"];
        }
    }

    if (!shape) {
        shape = shapes["L2J-M"]; // default
    }

    const svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="${shape.viewBox}" width="${shape.w}" height="${shape.h}">
        <path d="${shape.path}" fill="yellow" stroke="black" stroke-width="${shape.strokeScale}" />
    </svg>`;

    const svgBase64 = `data:image/svg+xml;base64,${btoa(svg)}`;
    return svgBase64;
}